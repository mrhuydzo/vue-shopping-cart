<template>
    <tr>
        <th scope="row">{{index + 1}}</th>
        <td>{{product.name}}</td>
        <td>{{formatPrice}}</td>
        <td><input :value="cart.quantity" type="number" min="1"></td>
        <td><strong>{{formatTotal}}</strong></td>
        <td>
            <a class="label label-info update-cart-item" href="#" data-product="">Update</a>
            <a @click.prevent="handleDelete" class="label label-danger delete-cart-item" href="#" data-product="">Delete</a>
        </td>
    </tr>
</template>
<script>
    import {toCurrency} from "../helper";
    import {NOTI_ACT_DELETE} from "./../constants/config";
    import {mapActions} from 'vuex'
    export default {
        name: 'cart-body-item',
        data () {
            return {

            }
        },
        props: {
            cart: {type: Object},
            index: {type: Number}
        },
        computed: {
            product() {
                return this.cart.product
            },
            formatPrice() {
                return toCurrency(this.product.price,'USD','right')
            },
            formatTotal(){
                return toCurrency(this.product.price * this.cart.quantity,'USD','right')
            }
        },
        methods: {
            ...mapActions({
                'actDeleteCart' : 'cart/actDeleteCart'
            }),
            handleDelete() {
                if(confirm('Muon xoa a?')){
                    this.actDeleteCart(this.cart);
                    this.$notify(NOTI_ACT_DELETE);
                }
            }
        }
    }
</script>